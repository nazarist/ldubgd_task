public class Help {


    protected static void out(String... data)
    {
        for (String item : data){
            System.out.println(item);
        }
    }


    protected static void out(int... data)
    {    
        for (int item : data){
            System.out.println(item);    
        }
    }

    
    protected static void out(double... data)
    {
        for (double item : data){
            System.out.println(item);
        }
    }
}
